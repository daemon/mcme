#define swap16(x) do{\
  x = (x >> 8) | (x << 8);\
} while (0)

#define swap24(x) do {\
  x = (x & 0x00ff0000) | ((x & 0xff000000) >> 16) | ((x & 0x0000ff00) << 16);\
} while (0)

#define swap32(x) do {\
  x = ((x & 0xff000000) >> 24) | ((x & 0x00ff0000) >> 8) | ((x & 0x0000ff00) << 8) | ((x & 0x000000ff) << 24);\
} while (0)

